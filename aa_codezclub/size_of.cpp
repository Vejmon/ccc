#include <iostream>
using namespace std;

int main() {
  cout << "Number of bytes for int: " << sizeof(int) << endl;
  cout << "Number of bytes for char: " << sizeof(char) << endl;
  cout << "Number of bytes for bool: " << sizeof(bool) << endl;
  cout << "Number of bytes for double: " << sizeof(double) << endl;

  return 0;
}
